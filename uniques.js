const fs = require("fs/promises");

function writeFiles() {
	return new Promise((resolve, reject) => {
		let resolvedPromises = 0;

		UNIQUES.forEach(data => {
			let randomIndex = defined[data.image] || Math.floor(7777 * Math.random());

			let attributes = [];
			for (let key in data.traits) {
				let value = data.traits[key];

				attributes.push({trait_type: key, value: value});
			}

			let metadata = JSON.stringify({
				"name": `DimWits #${randomIndex}`,
				"description": "DimWits is a collection of 7,777 NFT’s that also serve as a “ticket.” Each DimWit holder has access to exclusive content.",
				"image": `https://storage.googleapis.com/dimwitsnft/${randomIndex}.png`,
				"secret": `https://storage.googleapis.com/dimwitsnft/${randomIndex}_noBG.png`,
				"attributes": attributes
			});

			fs.readFile(`./manuals/${data.image}2.png`)
				.then(res => {
					fs.writeFile(`./output/${randomIndex}.png`, res)
						.then(res => {
							fs.readFile(`./manuals/${data.image}1.png`)
								.then(res => {
									fs.writeFile(`./output/${randomIndex}_noBG.png`, res)
										.then(res => {
											fs.writeFile(`./output/${randomIndex}.json`, metadata)
												.then(res => {
													resolvedPromises += 1;

													if (resolvedPromises == UNIQUES.length) {
														resolve();
													}
												});
										});
								});
						});
				});
		});
	});
}

